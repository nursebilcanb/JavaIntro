package homeworkDay3;

public class User {
	
	int id;
	String firstName;
	String lastName;
	String password;
	String email;
	
	public User(int id, String firstName, String lastName, String password, String email) {
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.password = password;
		this.email = email;
	}
	
	

}
